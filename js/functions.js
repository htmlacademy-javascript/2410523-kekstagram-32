function checksStringPalin (line) {
  const string = line.replaceAll(' ', '').toLowerCase();
  let empty = '';

  for (let ind = string.length - 1; ind >= 0; ind --){
    empty += string[ind];
  }
  return string === empty;
}

checksStringPalin();
